import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import crypto from "node:crypto";
import { createClient } from "@supabase/supabase-js";

const HIDDEN_ARTIST_IDS = [];

function loadEnvFile() {
  const envPath = path.resolve(".env");

  if (!fs.existsSync(envPath)) {
    return;
  }

  const contents = fs.readFileSync(envPath, "utf8");

  for (const line of contents.split(/\r?\n/)) {
    const trimmed = line.trim();

    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const separatorIndex = trimmed.indexOf("=");

    if (separatorIndex === -1) {
      continue;
    }

    const key = trimmed.slice(0, separatorIndex).trim();
    const value = trimmed.slice(separatorIndex + 1).trim();

    if (!(key in process.env)) {
      process.env[key] = value;
    }
  }
}

function getRequiredEnv(name) {
  const value = process.env[name];

  if (!value) {
    throw new Error(`Missing required environment variable: ${name}`);
  }

  return value;
}

function stableUuid(prefix, value) {
  const hash = crypto.createHash("md5").update(`${prefix}:${value}`).digest("hex");
  return [
    hash.slice(0, 8),
    hash.slice(8, 12),
    `4${hash.slice(13, 16)}`,
    `8${hash.slice(17, 20)}`,
    hash.slice(20, 32),
  ].join("-");
}

function normalizeText(value) {
  return (value || "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function includesNormalized(haystack, needle) {
  const normalizedHaystack = normalizeText(haystack);
  const normalizedNeedle = normalizeText(needle);

  if (!normalizedHaystack || !normalizedNeedle) {
    return false;
  }

  return normalizedHaystack.includes(normalizedNeedle);
}

async function youtubeApi(pathname, searchParams) {
  const apiKey = getRequiredEnv("YOUTUBE_API_KEY");
  const url = new URL(`https://www.googleapis.com/youtube/v3/${pathname}`);

  Object.entries(searchParams).forEach(([key, value]) => {
    if (value !== undefined && value !== null && value !== "") {
      url.searchParams.set(key, String(value));
    }
  });

  url.searchParams.set("key", apiKey);

  const response = await fetch(url);
  const data = await response.json();

  if (!response.ok) {
    throw new Error(
      `YouTube API request failed for ${pathname}: ${response.status} ${
        data?.error?.message || response.statusText
      }`
    );
  }

  return data;
}

async function searchYoutubeVideos(query) {
  const data = await youtubeApi("search", {
    part: "snippet",
    maxResults: 5,
    q: query,
    type: "video",
    videoCategoryId: "10",
  });

  return data.items || [];
}

async function fetchVideoDetails(videoIds) {
  if (!videoIds.length) {
    return [];
  }

  const data = await youtubeApi("videos", {
    part: "snippet,contentDetails,status",
    id: videoIds.join(","),
  });

  return data.items || [];
}

function scoreCandidate({ artistName, releaseTitle, video }) {
  const title = video?.snippet?.title || "";
  const channelTitle = video?.snippet?.channelTitle || "";
  const description = video?.snippet?.description || "";
  const combinedText = `${title} ${channelTitle} ${description}`;
  let score = 0;

  if (includesNormalized(title, releaseTitle)) {
    score += 45;
  }

  if (includesNormalized(combinedText, artistName)) {
    score += 28;
  }

  if (includesNormalized(channelTitle, artistName)) {
    score += 18;
  }

  if (includesNormalized(channelTitle, `${artistName} topic`)) {
    score += 16;
  }

  if (includesNormalized(description, "provided to youtube by")) {
    score += 12;
  }

  if (includesNormalized(description, "auto-generated by youtube")) {
    score += 10;
  }

  if (includesNormalized(title, "official audio")) {
    score += 7;
  }

  if (includesNormalized(title, "topic")) {
    score += 5;
  }

  if ((video?.status?.privacyStatus || "") !== "public") {
    score -= 100;
  }

  return score;
}

async function findBestYoutubeMatch({ artistName, releaseTitle }) {
  const searchResults = await searchYoutubeVideos(`${artistName} ${releaseTitle}`);
  const videoIds = searchResults
    .map((item) => item?.id?.videoId)
    .filter(Boolean);
  const videoDetails = await fetchVideoDetails(videoIds);
  const detailById = new Map(videoDetails.map((item) => [item.id, item]));

  const candidates = videoIds
    .map((videoId) => detailById.get(videoId))
    .filter(Boolean)
    .map((video) => ({
      video,
      score: scoreCandidate({ artistName, releaseTitle, video }),
    }))
    .sort((a, b) => b.score - a.score);

  const best = candidates[0] || null;

  if (!best || best.score < 55) {
    return {
      matched: false,
      score: best?.score || 0,
      topTitle: best?.video?.snippet?.title || null,
      url: null,
    };
  }

  return {
    matched: true,
    score: best.score,
    topTitle: best.video.snippet?.title || null,
    url: `https://www.youtube.com/watch?v=${best.video.id}`,
  };
}

async function main() {
  loadEnvFile();

  const supabase = createClient(
    getRequiredEnv("VITE_SUPABASE_URL"),
    getRequiredEnv("SUPABASE_SERVICE_ROLE_KEY")
  );

  const [{ data: releases, error: releasesError }, { data: artists, error: artistsError }, { data: releaseLinks, error: releaseLinksError }] =
    await Promise.all([
      supabase
        .from("releases")
        .select("id, artist_id, title, slug")
        .not("artist_id", "in", `(${HIDDEN_ARTIST_IDS.join(",")})`),
      supabase.from("artists").select("id, name"),
      supabase.from("release_links").select("id, release_id, platform, url"),
    ]);

  if (releasesError) {
    throw releasesError;
  }

  if (artistsError) {
    throw artistsError;
  }

  if (releaseLinksError) {
    throw releaseLinksError;
  }

  const artistNameById = new Map((artists || []).map((artist) => [artist.id, artist.name]));
  const youtubeLinkedReleaseIds = new Set(
    (releaseLinks || [])
      .filter((link) => (link.platform || "").toLowerCase() === "youtube")
      .map((link) => link.release_id)
  );

  const targetReleases = (releases || [])
    .filter((release) => !youtubeLinkedReleaseIds.has(release.id))
    .filter((release) => !HIDDEN_ARTIST_IDS.includes(release.artist_id))
    .map((release) => ({
      ...release,
      artistName: artistNameById.get(release.artist_id) || "",
    }))
    .filter((release) => release.artistName && release.title);

  const syncedLinks = [];
  const skipped = [];

  for (const release of targetReleases) {
    try {
      const result = await findBestYoutubeMatch({
        artistName: release.artistName,
        releaseTitle: release.title,
      });

      if (!result.matched || !result.url) {
        skipped.push({
          artist: release.artistName,
          release: release.title,
          slug: release.slug,
          score: result.score,
          topTitle: result.topTitle,
          reason: "No confident YouTube match found",
        });
        continue;
      }

      syncedLinks.push({
        id: stableUuid("youtube-release-link", result.url),
        release_id: release.id,
        platform: "youtube",
        url: result.url,
        embed_url: result.url,
        artist: release.artistName,
        release: release.title,
        score: result.score,
      });
    } catch (error) {
      skipped.push({
        artist: release.artistName,
        release: release.title,
        slug: release.slug,
        reason: error instanceof Error ? error.message : "Unknown error",
      });
    }
  }

  if (syncedLinks.length) {
    const rows = syncedLinks.map(({ artist, release, score, ...row }) => row);
    const { error: upsertError } = await supabase
      .from("release_links")
      .upsert(rows, { onConflict: "id" });

    if (upsertError) {
      throw upsertError;
    }
  }

  console.log(
    JSON.stringify(
      {
        ok: true,
        checkedReleaseCount: targetReleases.length,
        syncedLinkCount: syncedLinks.length,
        syncedLinks: syncedLinks.map(({ artist, release, url, score }) => ({
          artist,
          release,
          url,
          score,
        })),
        skipped,
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
